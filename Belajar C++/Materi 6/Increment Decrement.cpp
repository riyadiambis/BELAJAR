#include<iostream>
using namespace std;

int main(){
    int x, y;
    //Increment
    //Post Increment
    x = 1;
    cout<<x++<<endl;
    cout<<x++<<endl;
    cout<<x++<<endl;

    cout<<"============================="<<endl;

    //Pre Increment
    y =12;
    cout<<++y<<endl;
    cout<<++y<<endl;
    cout<<++y<<endl;

    /*
    Sederhananya di sini angka akan dijumlahkan dengan angka di variabel itu sendiri
    */

}